def luas_persegi_panjang():
    p = int (input("Masukkan panjang Persegi panjang "))
    l = int (input("Masukkan lebar persegi panjang "))
    luas = p*l
    print ("Luas Persegi panjang adalah : ", luas)

luas_persegi_panjang()